// ── Build output for a grade ──
function buildGrade(titles, grade, prereqs) {
  const themes = {};
  const lits = {};
  const blms = {};

  for (const t of titles) {
    themes[t] = themeOverrides[t] || classifyTheme(t);
    lits[t] = literacyOverrides[t] || classifyLiteracies(t);
    blms[t] = bloomOverrides[t] || classifyBloom(t);
  }

  // Validate prereqs: both from and to must exist in titles
  const titleSet = new Set(titles);
  const validPrereqs = prereqs.filter(p => titleSet.has(p.from) && titleSet.has(p.to));

  return {
    subject: "chinese",
    grade: String(grade),
    generatedAt: "2026-06-20",
    kpCount: titles.length,
    prerequisites: validPrereqs,
    themes,
    literacies: lits,
    blooms: blms
  };
}

