// ── Pattern-based classifiers ──

function classifyTheme(t) {
  // chi_literacy: 基础字词、修辞基础知识
  if (/拼音|汉字|识字|书写|词汇/.test(t)) return 'chi_literacy';
  // chi_writing: 明确的写作技法、口语交际
  if (/写作|写人物|写法|写作方法|写作技巧|写作策略|写作规范|写事|写人|选材|详略|扩写|改写|续写|仿写|审题|立意|修改|文章结构|行文思路|中心突出|中心明确|语言简明|语言连贯|表达得体|即席讲话|演讲|辩论|倡议书|读后感|口语|应对|讲话技巧|演讲词|演讲稿|叙事要素|叙事六要素|夹叙夹议/.test(t)) return 'chi_writing';
  // chi_comprehensive: 综合性学习、探究活动
  if (/探究|实践|交流|规划|整理|搜集|低碳|孝亲|交友之道|语文生活|读书方法|阅读习惯|数字时代|和文化|自强不息|文化遗产|初中生活|回顾与纪念/.test(t)) return 'chi_comprehensive';
  // Everything else → chi_reading (阅读与鉴赏 is the default for most KPs)
  return 'chi_reading';
}

function classifyLiteracies(t) {
  const r = [];
  // culture indicators
  if (/儒|孔孟|孟子|庄子|古文|文言|古典|古诗|唐诗|宋词|诗经|乐府|神话|创世|寓言|传统文化|家书|家训|修身|格言|孝亲|敬老|诚信|和文化|大同|民本|忠|义|礼|讽谏|劝谏|劝学|表文|史传|咏史|革命|雷锋|航天|保尔|钢铁|科学家精神|民族|家国|义利/.test(t)) r.push('lit_chi_culture');
  // aesthetic indicators
  if (/意境|意象|抒情|美感|诗意|诗化|象征|托物|借景|咏物|散文诗|田园诗|山水|思乡|离别|豪情|悲慨|哲理|讽喻|巧思|闲愁|婉约|旷达|豪放|崇高|朦胧|空灵|禅意|白描|留白|审美|韵律|音韵|骈/.test(t)) r.push('lit_chi_aesthetic');
  // thinking indicators
  if (/思辨|批判|逻辑|论证|论点|论据|驳论|立论|对比|悬念|结构|线索|层次|多角度|分析|归纳|提炼|论证|辩证|想象|联想|推理|怀疑|创造/.test(t)) r.push('lit_chi_think');
  // language is the most common
  r.push('lit_chi_lang');
  // ensure 1-3
  if (r.length > 3) r.length = 3;
  return r;
}

function classifyBloom(t) {
  // create
  if (/创作|创写|改写|续写|扩写|仿写|起步|写作运用|综合运用|实践技巧|写作方法$|写作技巧$/.test(t)) return 'bloom_create';
  // evaluate
  if (/评价|批判|鉴赏方法|辩证思考|思辨|价值|判断|策略$/.test(t)) return 'bloom_evaluate';
  // analyze
  if (/分析|作用|特点|特色|手法|技巧|比较|对比|层次|意象|意境|象征|讽刺|夸张|艺术|叙事视角|情节|结构|安排|设置|线索|悬念|误会|详略|选材|组材/.test(t)) return 'bloom_analyze';
  // apply
  if (/运用|方法$|修辞|描写方法|白描|朗读|朗诵|背诵|默写|朗读与|说明方法|说明顺序|表达方式|描写$/.test(t)) return 'bloom_apply';
  // remember
  if (/积累|识记|背诵|记忆|常识|体例|格律|词牌|体裁/.test(t)) return 'bloom_remember';
  // understand (default)
  return 'bloom_understand';
}

