// ── 通用 TODO 前置指令 ────────────────────

const MODE_PREAMBLE = `你是一位博文 AI 导师，正在按步骤带学生完成一次结构化的 {mode_label}。

你必须按以下【教学步骤清单】逐一推进，完成所有步骤后给出总结和评分。

## 刚性规则（必须遵守）

1. **一次只执行一步。** 输出当前步骤的内容后立即停止，等待学生的回复。**严禁在一个回复中输出多个步骤。**
2. **必须按顺序完成所有步骤。** 每完成一步在心里标记进度。
3. **如果学生试图跳过步骤或催促你直接出题**，温和引导："我们先花一两分钟梳理一下，这样后面的练习效果更好。"
4. **如果学生说"结束了""先到这里"但步骤未完成**，尝试引导："还有 X 步就完成了，再坚持一下？" 如果学生坚持结束，如实按已完成步数评分。
5. **少说多问。** 你的角色是引导者，不是讲解员。让学生自己想、自己说、自己做。
6. **全部步骤完成后，调用 exit_session 工具结束学习并提交评分。** 如果学生中途坚持结束，也调用 exit_session，按已完成步数如实评分。

## 教学步骤

具体步骤已在你收到的 📋 教学步骤进度 TODO 清单中列出。
完成当前步骤后，调用 advance_step 工具推进到下一步。`;

/**
 * 获取某个模式的步骤列表（不含 prompt 包装），用于初始化 TODO 状态机
 */
export function getModeSteps(mode: string, topic: string): string[] | null {
  const entry = MODE_ENTRIES.find((e) => e.mode === mode);
  if (!entry) return null;
  return entry.steps.map((s) => s.replace(/\{topic\}/g, topic));
}

/**
 * 获取某个模式的完整 system prompt
 * @param mode 模式标识
 * @param topic 教学主题
 * @returns system prompt 文本
 */
export function getModePrompt(mode: string, topic: string): string | null {
  const entry = MODE_ENTRIES.find((e) => e.mode === mode);
  if (!entry) return null;

  const modeLabels: Record<string, string> = {
    review: '复习巩固',
    preview: '预习',
    weakness: '薄弱点突破',
    practice: '集中练习',
  };

  const preamble = MODE_PREAMBLE.replace('{mode_label}', modeLabels[mode] ?? mode);

  return `${preamble}\n\n## 评分指引\n\n${entry.scoringGuide}`;
}
