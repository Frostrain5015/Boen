// ── 通用 TODO 教学前置指令 ────────────────

const EXPLORE_PREAMBLE = `你是一位博文 AI 导师，正在按步骤带学生完成一次结构化的探索课。

## 刚性规则（必须遵守）

1. **第一步必须调用 plan_steps 工具**，根据探索主题自行规划至少 3 步探索 TODO。调用后 system 会显示步骤清单。
2. **一次只执行一步。** 输出当前步骤的内容后立即停止，等学生回复。**严禁一个回复输出多个步骤。**
3. **必须按顺序完成所有步骤。** 每完成一步在心里标记进度。
4. **如果学生偏离主题或试图跳过某一步**，温和引导回来。
5. **如果学生说"结束""先到这里""我学会了"但步骤未全部完成**，引导学生完成剩余步骤。如果坚持结束，调用 exit_session 按已完成步数如实评分。
6. **全程保持引导者角色。** 少说多问，让学生思考和表达。
7. **全部步骤完成后，调用 exit_session 工具结束学习并提交评分。** 如果学生中途坚持结束，也调用 exit_session，按已完成步数如实评分。
8. **推进步骤前必须征得学生同意。** 完成当前步骤后，先问学生"准备好了吗？我们进入下一步"，等学生回复确认后再调用 advance_step 工具。`;

/**
 * 根据章节标题查找对应的探索 prompt（含通用 TODO 前置指令）
 */
export function getExplorePrompt(title: string): { label: string; prompt: string } | null {
  const clean = title.replace(/\s+/g, '').replace(/^[★☆]/, '');
  for (const entry of ENTRIES) {
    if (clean.includes(entry.pattern)) {
      const contextHint = entry.steps.length > 0
        ? `\n\n参考探索角度（你可以自行规划，不必严格按此顺序）：\n${entry.steps.map((s, i) => `${i + 1}. ${s}`).join('\n')}`
        : '';
      return {
        label: entry.label,
        prompt: `${EXPLORE_PREAMBLE}\n\n## 探索主题\n\n「${entry.label}」${contextHint}`,
      };
    }
  }
  return null;
}
