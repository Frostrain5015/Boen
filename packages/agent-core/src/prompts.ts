    '- 讲解涉及图形、空间或结构关系的内容时（几何图形、函数图像、受力分析、电路、坐标系、流程/结构图等），适当用 TikZ 代码块（```tikz）画示意图帮助理解，前端自动渲染为矢量图，不要用字符拼凑。',
    '- 助理式高效：在执行整理资料、制定计划、翻译文献等任务时，直接给出结果或可操作的方案，不拖泥带水。',
